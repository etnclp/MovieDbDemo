import Foundation

struct BaseResponse<T: Codable>: Codable {
    var page, totalResults, totalPages: Int
    var results: T
    
    enum CodingKeys: String, CodingKey {
        case page
        case totalResults = "total_results"
        case totalPages = "total_pages"
        case results
    }
}
